package com.gps.bean;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.Date;


public class Decano implements Serializable {
    private static final long serialVersionUID = 1L;    
    private BigDecimal id;
    private Date fechaini;
    private Date fechafin;
    private String estadoActual;
    private Facultad facultad;
    private Docente docente;

    public Decano() {
    }

    public Decano(BigDecimal id) {
        this.id = id;
    }

    public Decano(BigDecimal id, Date fechaini, String estadoActual) {
        this.id = id;
        this.fechaini = fechaini;
        this.estadoActual = estadoActual;
    }

    public BigDecimal getId() {
        return id;
    }

    public void setId(BigDecimal id) {
        this.id = id;
    }

    public Date getFechaini() {
        return fechaini;
    }

    public void setFechaini(Date fechaini) {
        this.fechaini = fechaini;
    }

    public Date getFechafin() {
        return fechafin;
    }

    public void setFechafin(Date fechafin) {
        this.fechafin = fechafin;
    }

    public String getEstadoActual() {
        return estadoActual;
    }

    public void setEstadoActual(String estadoActual) {
        this.estadoActual = estadoActual;
    }

    public Facultad getFacultad() {
        return facultad;
    }

    public void setFacultad(Facultad facultad) {
        this.facultad = facultad;
    }

    public Docente getDocente() {
        return docente;
    }

    public void setDocente(Docente docente) {
        this.docente = docente;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Decano)) {
            return false;
        }
        Decano other = (Decano) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.gps.bean.Decano[ id=" + id + " ]";
    }
    
}
