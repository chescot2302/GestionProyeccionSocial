    private void cargarDatosUpdate() {
        btnGuardar.setText("Actualizar");
        try {
            int fila = tablaDatos.getSelectedRow();
            int modelrow = tablaDatos.convertRowIndexToModel(fila);
            Docente obj = ((TableModelDocente) tablaDatos.getModel()).getValue(modelrow);
            lblId.setText(obj.getId().toString());
            txtNombres.setText(obj.getNombres());
            txtApPaterno.setText(obj.getAppaterno());
            txtApMaterno.setText(obj.getApmaterno());
            cbFacultad.setSelectedItem(obj.getEscuela().getFacultad().getSiglas());            
            cbFacultad.getEditor().setItem(obj.getEscuela().getFacultad().getSiglas());            
            cbmodesc.setData(LogicEscuela.listarEscuela(obj.getEscuela().getFacultad().getId()));
            cbEscuela.setModel(cbmodesc);
            cbEscuela.updateUI();
            cbEscuela.getEditor().setItem(obj.getEscuela().getDescripcion());
            cbGrado.setSelectedItem(obj.getGrado().getDescripcion());
            cbGrado.getEditor().setItem(obj.getGrado().getDescripcion());
        } catch (Exception ex) {
            ViewApplication.mensajeEstado.setText("Error");
            ViewApplication.mensajeEstado.setIcon(ViewLogin.picture.getIconBad());
        }
    }
    
    private void eliminar() {
        int fila = tablaDatos.getSelectedRow();
        int modelrow = tablaDatos.convertRowIndexToModel(fila);
        Docente docente = ((TableModelDocente) tablaDatos.getModel()).getValue(modelrow);
        if (LogicDocente.eliminarDocente(docente.getId())) {
            ((TableModelDocente) tablaDatos.getModel()).borrar(modelrow);
            ViewApplication.mensajeEstado.setText("Eliminado correctamente");
            ViewApplication.mensajeEstado.setIcon(ViewLogin.picture.getIconGood());
        } else {
            ViewApplication.mensajeEstado.setText("Error");
            ViewApplication.mensajeEstado.setIcon(ViewLogin.picture.getIconBad());
        }
    }
    
    private void cleaning(){
       txtNombres.setText(null);
       txtApPaterno.setText(null);
       txtApMaterno.setText(null);
       btnGuardar.setText("Guardar");
       lblId.setText(null);
   }
    private void mantenimiento(){
        BigDecimal idEscuela=null;
        BigDecimal idGrado=null;        
        String nombres=null;
        String apPaterno=null;
        String apMaterno=null;        
        BigDecimal estado=null;
        BigDecimal id=null;
        switch (btnGuardar.getText()) {
            case "Guardar":
                try{
                idEscuela=cbmodesc.getElement(cbEscuela.getSelectedIndex()).getId();
                idGrado=cbmodgrad.getElement(cbGrado.getSelectedIndex()).getId();
                nombres=txtNombres.getText();
                apPaterno=txtApPaterno.getText();
                apMaterno=txtApMaterno.getText();
                estado=LogicDocente.registrarDocente(nombres, apPaterno, apMaterno, idEscuela, idGrado);                
                if(estado.compareTo(BigDecimal.ONE)==0){
                    modeloDatos.setData(LogicDocente.listarDocente(BigDecimal.ZERO));
                    modeloDatos.fireTableDataChanged();
                    cleaning();
                }
                }catch(Exception ex){
                    ViewApplication.mensajeEstado.setText("Error");
                    ViewApplication.mensajeEstado.setIcon(ViewLogin.picture.getIconBad());
                }
                break;
            case "Actualizar":    
                try{
                idEscuela=cbmodesc.getElement(cbEscuela.getSelectedIndex()).getId();
                idGrado=cbmodgrad.getElement(cbGrado.getSelectedIndex()).getId();
                nombres=txtNombres.getText();
                apPaterno=txtApPaterno.getText();
                apMaterno=txtApMaterno.getText();
                id=BigDecimal.valueOf(Double.valueOf(lblId.getText()));
                estado=LogicDocente.actualizarDocente(id,nombres, apPaterno, apMaterno, idEscuela, idGrado);                
                if (estado.compareTo(BigDecimal.valueOf(2)) == 0) {
                    modeloDatos.setData(LogicDocente.listarDocente(BigDecimal.ZERO));
                    modeloDatos.fireTableDataChanged();                    
                    cleaning();
                }
                }catch(Exception ex){
                    ViewApplication.mensajeEstado.setText("Error");
                    ViewApplication.mensajeEstado.setIcon(ViewLogin.picture.getIconBad());
                }
                break;
        }
    }
