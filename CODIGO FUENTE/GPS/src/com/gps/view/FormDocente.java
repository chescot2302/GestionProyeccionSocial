    ActionListener actionListener=new ActionListener(){

        @Override
        public void actionPerformed(ActionEvent e) {
            if(e.getSource().equals(cbFacultad)){ 
                try{                                    
                cbFacultad.getEditor().setItem(cbFacultad.getSelectedItem());
                cbmodesc.setData(LogicEscuela.listarEscuela(cbmodfac.getElement(cbFacultad.getSelectedIndex()).getId()));
                cbEscuela.setModel(cbmodesc);        
                cbEscuela.updateUI();    
                cbEscuela.getEditor().setItem(null);                
                }catch(Exception ex){
                    cbmodesc.setData(new ArrayList<Escuela>());
                    cbEscuela.setModel(cbmodesc);
                    cbFacultad.getEditor().setItem(null);
                    cbEscuela.getEditor().setItem(null);                
                }
            }else if(e.getSource().equals(btnGuardar)){
                mantenimiento();
            }else if(e.getSource().equals(btnCancelar)){
                cleaning();
            }/*else if(e.getSource().equals(btnAddEscuela)){
                viewPanelEscuela();
            }*/
        }        
    };
    
    MouseAdapter mouseAdapter = new MouseAdapter() {

        @Override
        public void mouseClicked(MouseEvent e) {
            if (e.getClickCount() == 2) {
                cargarDatosUpdate();
            }
        }
    };
    
    KeyListener keyListener = new KeyListener() {

        @Override
        public void keyTyped(KeyEvent e) {
        }

        @Override
        public void keyPressed(KeyEvent e) {
            if (e.getSource().equals(tablaDatos)) {
                if (e.getKeyCode() == KeyEvent.VK_DELETE) {
                    int n = JOptionPane.showConfirmDialog(null, "¿Desea en realidad eliminar el registro?", "Information", JOptionPane.YES_NO_OPTION);
                    if (n == JOptionPane.YES_OPTION) {
                        eliminar();
                    }
                }
            }
        }

        @Override
        public void keyReleased(KeyEvent e) {
        }
    };
